     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $this->validate($request,[
            'account_name'=>'required',
            'account_description'=>'required',
             'surname'=>'required',
             'creator_email'=>'required'
        ]);
       
        $post=new Accounts;
        $post->account_name=$request->input('account_name');
        $post->account_description=$request->input('account_description');
        $post->surname=$request->input('surname');
        $post->email=$request->input('creator_email');
        $post->save();
        return redirect('account/show')->with('success','Expense Added');
    }

